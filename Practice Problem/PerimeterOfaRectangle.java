public class PerimeterOfaRectangle {
    public static void main(String[] args) {
        double length = 8.0;
        double width = 5.0;
        double perimeter = 2 * (length + width);
        System.out.println("The perimeter of a rectangle with length " + length + " and width " + width + " is: " + perimeter);
    }
}
